let i;
i=10;
i++;// take value of i , add 1, store it back in i--post incremenet
console.log(i);//11

i=10;
++i;//pre increment
console.log(i);//11

let a,b;
a=10;
b=++a;//pre incremenet--> 1st incremenet and then assign updated value to b
console.log(a);
console.log(b);


a=10;
b=a++;//post incremenet--> 1st assign current value to b and later increment value of a
console.log(a);
console.log(b);
